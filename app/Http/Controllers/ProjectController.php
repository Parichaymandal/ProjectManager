     */
    public function store(Request $request)
    {
        $input = $request->all();
        $job = Job::create($input);

        $emp = Employee::find($input['id']);
        $emp->jobs()->save($job);
        $user = Auth::user();
        $user->employee->jobs()->save($job);


        return redirect('main_dashboard');
    }
